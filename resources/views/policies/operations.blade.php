<strong class="w3-purple">1. Banking Operations Group</strong><br><br>
<p style="word-wrap:break-word;">Banking Operations is the Group that ensures effective delivery of the
bank‟s products and services. Its major tasks are ensuring that the services
of the bank are delivered effectively, efficiently and cost-effectively. The
functions carried out by this Group include: Customer Services, Teller
Services, Clearing, IT services, Cash Operations and Correspondent Banking
