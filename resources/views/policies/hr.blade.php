<br>






<strong class="w3-red">Powers of Supervisors within the Disciplinary Committee Process</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> (1) A supervisor can initiate a higher disciplinary process against
his/her subordinate by referring the case to Internal Control (or
Inspection as the case may be) for further investigation if that
would lead to the sitting of a disciplinary committee.</li><br>  
<li class="w3-card"> (2) Only cases that attract sanctions above Caution Letter shall bereferred to a DC.</li><br>      
<li class="w3-card"> (3) The issuance of immediate suspension before the sitting of a
disciplinary committee shall be restricted to only cases of
suspected misapplication, misappropriation, conversion, and
integrity issues or where the suspension is necessary to allow for
unimpeded investigation of an allegation.</li><br>      
<li class="w3-card"> (4) The authority to suspend a staff above Deputy Manager (Step 1)
shall be vested with all ED’s.</li><br>      
<li class="w3-card"> (5) The disciplinary committee shall within a period not exceeding 3
days sit on any case that warrants immediate suspension.
Request for further extension shall be only on sufficient justification, but shall not be for a period exceeding 2days. The
extension shall be approved by ED.</li><br>   
<li class="w3-card"> (6) In all cases, all letters of suspension can only be issued by
Human Resource Management Group</li><br>   

  </ul>
</div>
<br><br>







<strong class="w3-red">The Disciplinary Appeal Process</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> (1) Staff shall have right of appeal against any disciplinary sanction,
whether recommended directly by a supervisor or by a
disciplinary committee.</li><br>  
<li class="w3-card"> (2) Appeals against sanctions recommended by the, CSM, Unit
Heads shall be heard by the Group DC</li><br>      
<li class="w3-card"> (3) Appeal against sanctions recommended by supervisors of less
than Deputy Manager (Step 1) shall be heard at the Executive
Management DC where final decision shall be taken.</li><br>      
<li class="w3-card"> (4) An appeal however shall not cause the sanction to be suspended but
the sanction shall be withdrawn if the appeal is upheld.</li><br>      
<li class="w3-card"> (5) The right of appeal shall lapse 5 working days after the staff was
advised of the sanction by the Human Capital Management
Group. The date of mailing the suspension letter to the staff shall be
the effective countdown date for the 5 working days right of
appeal by staff.</li><br>   
<li class="w3-card"> (6) In all cases, all letters of suspension can only be issued by
Human Resource Management Group</li><br>   
   
  </ul>
</div>
<br><br>

<strong class="w3-red">Justification</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> The recommended appeal process is consistent with our guiding
philosophy of fairness, equity and justice that guarantees employees the right
to fair hearing</li><br>  
<li class="w3-card"> The disciplinary process shall not only be fair but must be seen to be fair
by relevant parties</li><br>      
<li class="w3-card">The process guides against abuse by both the appellant and members of
the relevant DCs</li><br>      
  </ul>
</div>


<br><br>


<strong class="w3-purple">Remuneration of Staff under suspension</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> (1) Staff on suspension shall not be entitled to monthly salary subject to a
minimum suspension period of 3 months. Withheld salaries shall,
however be refunded if the case is determined in favour of the staff.</li><br>  
<li class="w3-card"> (2) Employees whose contract of employment is determined by
resignation of the staff or termination by the employer shall be
entitled to receive terminal benefits, as provided in the bank’s
Human Capital Management Group.</li><br>      
<li class="w3-card"> (3) Staff who are dismissed from the Bank’s employment shall forfeit all
accrued benefits, in line with the bank’s Human Resource
Management Group policy</li><br>         
  </ul>
</div>
<br><br>

<strong class="w3-purple">Justification</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card">  In line with the guiding principle of fairness, justice and equity</li><br>  
<li class="w3-card"> Also portrays the bank as a caring organization</li><br>      
<li class="w3-card">The process guides against abuse by both the appellant and members of
the relevant DCs</li><br>      
  </ul>
</div>


<br><br>

<strong class="w3-purple">Impact of sanctions on staff Performance Appraisal</strong><br>
<p>Sanctions shall have the following negative appraisal scoring (i.e.
deduction from the staff’s gross appraisal score) within a 12
month appraisal cycle:</p><br>


<div class="text-box" >
  <ul>
