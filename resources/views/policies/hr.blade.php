<li class="w3-card"> (b) Caution Letter – 5%</li><br>      
<li class="w3-card">(c) First Warning Letter – 10%</li><br>      
<li class="w3-card">(d) Final Waning -15% (and disqualification for promotion during the year)</li><br>      
<li class="w3-card">(e) Suspension – 20% (and disqualification during annual promotion)</li><br>      
<li class="w3-card">(f) Demotion – automatic disqualification for promotion for 12calendar months.</li><br>      
<li class="w3-card">(c) First Warning Letter – 10%</li><br>      
  </ul>
</div>



