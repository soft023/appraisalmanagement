<h1 class="w3-text-purple" style="font-size: 35px" ><strong>HRM DISCIPLINARY PROCESS AND SANCTIONS GRID   <span class="w3-text-red">POLICY</span> (FIRST EDITION)</strong></h1><br>
</div>
<div class="col-md-12 col-sm-12 col-xs-12" style="word-wrap:initial; font-family: futura lt">

<h4 class="w3-red text-center"><strong>Objectives of the Disciplinary Process</strong></h4>
<strong class="w3-red">Overall Objectives</strong><br><br>
<p style="word-wrap:break-word;">To build a disciplinary process that conforms to the basic tenets of justice, protects employees against abuse or victimization andenables the institution maintain orderliness in its operations
</p>
<strong class="w3-red">Specific Objectives</strong><br><br>
<p>We seek to establish a disciplinary process that:</p>
<div class="text-box" >
  <ul>
    <li class="w3-card">  Ensures disciplined approach and orderliness in our business
operations</li><br>  
    <li class="w3-card">  Strengthens Internal Controls</li><br>  
    <li class="w3-card">  Is fair, just, equitable and accessible to all</li><br>   
    <li class="w3-card">  Ensures a culture of compliance with the bank’s rules and regulations</li><br>   
    <li class="w3-card">  Maintains corporate values and creates a professional environment</li><br>   
    <li class="w3-card">  Eliminates arbitrariness in the administration of justice</li><br>   
  </ul>
</div>


<h4 class="w3-purple text-center"><strong>Our Guiding Principles</strong></h4>
<p>The Disciplinary process shall be guided by the following :</p>
<strong class="w3-purple">Justice</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card">  Ensures disciplined approach and orderliness in our business operations</li><br>  
    <li class="w3-card">  The Disciplinary process shall guarantee the right of employees to appeal against perceived injustice or abuse</li><br>  
    <li class="w3-card">  Every staff appearing before the disciplinary Committee, D.C. shall be given the benefit of doubt</li><br>    
  </ul>
</div>




<strong class="w3-purple">Fairness</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card">  Sanctions shall not be imposed on an employee without a formal query
and reasonable time for response. The employee’s response to
the query shall as much as possible be in writing and shall be signed
by the employee.</li><br>  
    <li class="w3-card"> Every employee appearing before a disciplinary committee shall be
given the opportunity of fair hearing, except where the employee
declines an invitation.</li><br>  
    <li class="w3-card">  Every staff appearing before the disciplinary Committee, D.C. shall be given the benefit of doubt</li><br>    
  </ul>
</div>






<strong class="w3-purple">Equity</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card">  Sanctions shall be commensurate with the offence</li><br>  
    <li class="w3-card"> Sanctions shall not be discretionary or discriminatory but consistent with
the disciplinary process</li><br>  
    <li class="w3-card"> Where a staff declines an invitation to appear before a DC, the DC
shall hold in his/her absence and the decision of that DC shall be
binding on such staff</li><br>    
    <li class="w3-card">  All DC sittings regarding fraud of any kind shall be preceded by an
investigation by Internal Control or Inspection Unit to determine the
involvement of the staff in the matter and the report of the investigation
shall be presented to the committee by the investigator.</li><br>    
    
  </ul>
</div>





<strong class="w3-purple">Promptness/Timeliness</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> Investigation of disciplinary cases shall be concluded within three
days.</li><br>  
    <li class="w3-card"> Extension of the investigation period can be granted by the Head,
Group Internal Audit on sufficient justification, which shall not
exceed 2 days.</li><br>  
    <li class="w3-card"> Any further extension of time shall be approved by the ED, Business
Development, which shall not exceed one week.</li><br>    
    <li class="w3-card"> A Disciplinary Committee meeting shall be convened not more than three
working days after conclusion of investigations</li><br>    
    
  </ul>
</div>




<strong class="w3-purple">Reformatory /Corrective Impact</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> The Disciplinary process shall emphasize a reformatory/corrective
process, rather than being punitive</li><br>  
    <li class="w3-card"> Terminal sanctions shall apply only in severe situations such as fraud and
questionable integrity and other offences bordering on
gross/professional misconduct as contained in this grid/policy,
Human Resources manual and employment agreement</li><br>      
  </ul>
</div>





<strong class="w3-purple">Segregation of Powers</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> Disciplinary powers shall be concentrated more in DCs rather than
individual officers. However, to maintain discipline, orderliness and
decorum, supervisors shall be granted restricted sanction powers over their
subordinates, ranging from caution letters to suspension.</li><br>  
<li class="w3-card"> There shall be segregated of functions between the relevant DC, and
implementation of the recommendations of the DC</li><br>      
  </ul>
</div>






<strong class="w3-purple">DCs must be Accessible and Affordable</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card">The disciplinary process shall be easily accessible to every employee at no
cost, except in appeal cases where the employee shall bear his/her
expenses; these shall, however, be refunded should the appeal be
upheld</li><br>     
  </ul>
</div>




<strong class="w3-purple">Support for Enterprise-wide Risk management</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card">The disciplinary process shall support a strong and robust risk
management framework with a right to comment on the
recommendations of DCs within one week of receipt of the DC report,
which shall then be referred to EXCO</li><br>     
  </ul>
</div>





<strong class="w3-purple">Conduct of Disciplinary Committees</strong><br><br>
<div class="text-box" >
  <ul>
 <li class="w3-card"> Decisions at the DC shall be by simple majority. There shall be no veto of a
DC ruling.</li><br>  
<li class="w3-card"> Disciplinary matters involving members of a DC shall be handled by the next
higher DC with at leastone member of EXCO inattendance.</li><br>      
  </ul>
</div>




