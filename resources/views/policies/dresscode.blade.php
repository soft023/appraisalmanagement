<div class="col-md-12 col-sm-12 col-xs-12" style="word-wrap:initial; font-family: futura lt">

<h4 class="w3-red text-center"><strong>INTRODUCTION</strong></h4>
<p style="word-wrap:break-word;">All employees of Balogun Gambari Microfinance Bank Limited are the Bank’s representative through whom we communicate an image of professionalism. <br>It is therefore expected, that their appearance and demeanor must be professional at all times in the performance of duties. The policy sets out basic guidelines with examples of inappropriate attire and appearance.<br><br>
Violations of the policy can range from inappropriate clothing items to offensive perfumes and body odor.
</p><br><br>


<h4 class="w3-purple text-center"><strong>OBJECTIVE</strong></h4>
<p style="word-wrap:break-word;">The employees of Balogun Gambari Microfinance Bank contribute to the corporate culture and reputation in the way they present themselves. A professional appearance is essential to a favorable impression with customers, regulators and company shareholders (owners). Good grooming and appropriate dress reflect employee pride and inspire confidence on the part of such persons.
</p><br><br>



<h4 class="w3-red text-center"><strong>RELIGIOUS DRESSING</strong></h4>
<p style="word-wrap:break-word;">Balogun Gambari Mfb recognizes the importance of religious beliefs to persons within its workforce and will reasonably accommodate a staff’s religious beliefs in terms of workplace attire unless the accommodation creates an undue hardship. Head Covers or scarves that are required for religious purposes are allowed
</p><br><br>



<h4 class="w3-purple text-center"><strong>PROCEDURES</strong></h4>
<p style="word-wrap:break-word;">All supervisors may exercise reasonable discretion to determine appropriateness in employee dress and appearance. If a supervisor decides that an employee’s dress or appearance is not appropriate as outlined in this policy, he or she may take corrective action and require the employee to go home and change into conforming attire.<br><br>

If a staff member’s poor hygiene or use of too much perfume/cologne is an issue, the supervisor should discuss the problem with the staff member in private and should point out the specific areas to be corrected.<br>
Repeated disregard of Dress Code, Guidelines and Expectations will result in disciplinary action.

</p><br><br>




<h4 class="w3-red text-center"><strong>BASIC GUIDELINES FOR APPROPRIATE DRESSING</strong></h4>
<p style="word-wrap:break-word;">Basic elements for appropriate and professional business attire include socks or stockings and clothing that is in neat and clean condition.
</p>
<strong class="w3-purple">WOMEN</strong><br><br>

<div class="text-box" >
  <ul>
    <li class="w3-card" >  A.	SUIT: Black, brown, navy blue and grey suit colours are allowed. Suits should be worn at all times, Mondays-Wednesdays. </li><br>  
    <li class="w3-card"> B.	DRESSES- Dresses in the above mentioned colours may be worn. Sleeveless dresses must be worn with a jacket at all times. Dresses must not extend below or above the knees. White dresses are allowed.</li><br>  
    <li class="w3-card"> C.	SKIRTS- Skirts should be well fitting. Oversized and undersized skirts are not allowed. Skirts should not be overly tight that underwear lines are visible and should not be shorter than the knee and not longer than the ankles. It should be at a length at which you can sit comfortably in public. Tight skirts and miniskirts that ride halfway up the thigh are inappropriate for work. </li><br>   
    <li class="w3-card">  D.	SHIRTS/BLOUSES- Shirts and blouses should be well ironed.
All colours are permitted. However, colours must be pastel or somber and conservative. May be stripped or checked with not more than 3 colour blend.
</li><br>   
    <li class="w3-card">  E.	TROUSERS- Trousers should be the appropriate length, well-fitting and not overly tight. Three quarter trousers are not allowed.</li><br>  
    <li class="w3-card"> F.	SHOES- Court shoes, flat shoes, peep toes and sling back shoes may be worn. These may be either black, dark blue, or dark brown in leather, patent or suede material. Athletic shoes and any casual shoe are not acceptable in the office. However, Coloured shoes are only allowed on Fridays.
Shoes should be comfortable enough to support your ankles. Also, you should wear shoes with non-slip soles to keep your balance on tiled surfaces.
</li><br>  
    <li class="w3-card"> G.	HAIR- Hair styles must at all times be neat and subtle in appearance. Your hairstyles must complement your professional attire and not contrast it. Distracting styles in bright unatural colours are not allowed. </li><br>  
    <li class="w3-card"> H.	ACCESSORIES AND JEWELRY- Tasteful, professional scarves, belts, and jewelry are encouraged. Jewelry should be worn in good taste, classy, minimal with moderate size. Distracting dangling, loud and jewelry worn on body piercings and ankles are not allowed.</li><br>  
    <li class="w3-card">  I.	MAKEUP/PERFUMES- Makeup should be minimal .Overpowering fragrances should not be used. Some employees are allergic to the chemicals in perfumes therefore; perfumes must be worn with restraint.

