    <li class="w3-card"> B.	SHIRTS- Shirts may be white, light blue, navy blue, pink, lilac or purple. Shirts may be pin stripped with white background. However, pin stripped red and white shirt with white background may be allowed. Shirts must be well fitting and the sleeves must be the right length.
